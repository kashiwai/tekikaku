START TRANSACTION;

-- ================================================================
-- tokenカラムを追加（既に存在する場合はエラーになります）
-- ================================================================
ALTER TABLE dat_machine
ADD COLUMN token VARCHAR(255) NULL COMMENT '認証トークン（WebSocket/PeerJS用）'
AFTER signaling_id;

-- ================================================================
-- 既存の全マシンにランダムなトークンを生成して設定
-- ================================================================
UPDATE dat_machine
SET token = CONCAT(
    'net8_m',
    LPAD(machine_no, 3, '0'),
    '_',
    MD5(CONCAT(machine_no, UNIX_TIMESTAMP(), RAND()))
)
WHERE del_flg = 0
  AND (token IS NULL OR token = '');

-- ================================================================
-- 設定結果を確認
-- ================================================================
SELECT
    machine_no AS '機械番号',
    machine_cd AS '機械コード',
    signaling_id AS 'Signaling ID',
    LEFT(token, 30) AS 'トークン（先頭30文字）',
    CASE
        WHEN token IS NOT NULL THEN '✓ 設定済み'
        ELSE '✗ 未設定'
    END AS 'ステータス'
FROM dat_machine
WHERE del_flg = 0
ORDER BY machine_no;

